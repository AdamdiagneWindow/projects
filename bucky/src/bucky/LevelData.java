package bucky;

public class LevelData {

	private int level;
	private int score;
	private double time;
	
		
	public LevelData(int lev, int scor, double tim) {
			
			level = lev;
			score = scor;
			time = tim;
			
	}
	
	public int getLevel() {
		
		return level;
	}
	
	public int getScore() {
		
		return score;
	}
	
	public double getTime() { 
		
		return time;
	    
	}
		
		
		
		
	
}
